package array1d;

public class Employee {

    int id;
    String name;
    double salary;
    String department;
    int experience;

    public Employee(int id, String name, double salary, String department, int experience) {
        this.id = id;
        this.name = name;
        this.salary = salary;
        this.department = department;
        this.experience = experience;
    }
}
